package obsquraJavaClass;
public class ElseIfLadder
{
 public static void main(String args[])
  {
     int mark = 80;
	 if(mark<40)
	 {
	   System.out.println("Failed");
	 }
	 else if(mark<70)
	 {
	   System.out.println("Grade is C+");
	 }
	 else if(mark<=80)
	 {
	   System.out.println("Grade is B +");
	 }
	 else
	 {
	   System.out.println("Invalid Grade");
	 } 
  }
}